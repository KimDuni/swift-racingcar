import Foundation

struct StringCalculator {
    
    func 더하다(a: Int, b: Int) -> Int {
        return a + b
    }
    
    func 나누다(a: Int, b: Int) -> Int {
        return a / b
    }
    
    func 곱하다(a: Int, b: Int) -> Int {
        return a * b
    }
    
    func 빼다(a: Int, b: Int) -> Int {
        return a - b
    }
}
